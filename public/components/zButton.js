Vue.component('zbutton',{
	props:{
		click:{
			type:Function,
			default:function(){
				console.log("该按钮还没有click函数");
			}
		},
		bgcolor:{
			type:String,
			default:'rgba(76,175,80,0.8)'
		},
		fontcolor:{
			type:String,
			default:'white'
		},
		h:{
			type:String,
			default:'40px'
		},
		w:{
			type:String,
			default:'80px'
		},
		fontsize:{
			type:String,
			default:'16px'
		},
		cursorimg:{
			type:String,
			default:''
		},
		shadowcolor:{
			type:String,
			default:'green'
		},
		margin:{
			type:String,
			default:'auto'
		},
		fontfamily:{
			type:String,
			default:'微软雅黑'
		},
		float:{
			type:String,
			default:''
		},
		display:{
			type:String,
			default:''
		},
		position:{
			type:String,
			default:''
		},
		top:{
			type:String,
			default:''
		},
		left:{
			type:String,
			default:''
		}
	},
	data:function(){
		return{
			buttonStyle:{
				height:this.h,
				width:this.w,
				textAlign:'center',
				backgroundColor:this.bgcolor,
				color:this.fontcolor,
				fontSize:this.fontsize,
				fontFamily:this.fontfamily,
				fontWeight:'normal',
				borderRadius:'5px',
				verticalAlign:'middle',
				lineHeight:this.h,
				margin:this.margin,
				cursor:'url('+ this.cursorimg +'),pointer',
				float:this.float,
				display:this.display,
				position:this.position,
				top:this.top,
				left:this.left
			}
		}
	},
	methods:{
		enter:function(event){
			var color=this.shadowcolor;
			$(event.target).css('box-shadow',"0 0 8px 4px "+color);
		},
		leave:function(event){
			$(event.target).css('box-shadow','');
		}
	},
	template:
	'<div oncontextmenu="return false" '+
	'onselectstart="return false" '+
	'@click="click" '+
	':style="buttonStyle" '+ 
	'@mouseenter="enter" '+
	'@mouseleave="leave" '+
	'><slot></slot></div>'
})